#Program to convert km to metre
def convert(km):
    metre = 1000
    kilometer = km*metre
    print(km, "kilometre = ",kilometer, "metre")

convert(8)    

#Program to convert km to miles
def convert(km):
    miles = 0.62137
    kilometer = km*miles
    print(km, "kilometre = ",kilometer, "miles")

convert(8)    